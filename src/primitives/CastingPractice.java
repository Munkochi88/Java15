package primitives;

public class CastingPractice {
    public static void main(String[] args) {
        int a =(int) 2.5; //round it to down shows 2
        System.out.println(a);

        byte b;
        short c;
        char d;
        int e = 300;
        long f;
        float g;
        double h = 500.1;

        int ex = (int) h;
        System.out.println(ex);
        double el = e;
        System.out.println(el);


    }
}
